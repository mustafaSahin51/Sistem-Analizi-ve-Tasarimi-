  <h4>1. Araçlarınızın durumu nedir?</h4>
<p>Araçlarımız genellikle ikinci eldir ve her biri detaylı bir incelemeden geçer. Her araç için durumu ve geçmişi hakkında ayrıntılı bilgi sağlarız.</p>

<h4>2. Fiyatlarınızı nasıl belirliyorsunuz?</h4>
<p>Fiyatlarımız, aracın durumu, marka, model, yıl ve kilometre gibi faktörlere bağlı olarak belirlenir. Her araç için adil ve rekabetçi bir fiyat politikamız bulunmaktadır.</p>

<h4>3. Araçları inceleme veya test sürüşü yapabilir miyim?</h4>
<p>Elbette! İstediğiniz aracı inceleme veya test sürüşü yapma talebinde bulunabilirsiniz. Bunun için sitemizdeki iletişim bilgilerinden bize ulaşmanız yeterlidir.</p>

<h4>4. Kendi aracımı satabilir miyim?</h4>
<p>Evet, kendi aracınızı satmak istiyorsanız bize talep oluşturabilirsiniz. Uzman ekibimiz aracınızı değerlendirecek ve size en iyi teklifi sunacaktır.</p>

<h4>5. Araç değişimi veya takas yapabilir miyim?</h4>
<p>Takas veya araç değişimi taleplerinizi bize iletebilirsiniz. Mümkün olan durumlarda size yardımcı olmaktan memnuniyet duyarız.</p>

<h4>6. Satış işlemi nasıl gerçekleşir?</h4>
<p>İnternet üzerinden herhangi bir satış işlemi gerçekleştirmiyoruz. Ancak aracı satın almak veya satmak için talep oluşturabilirsiniz. Satış işlemleri fiziksel olarak galerimizde gerçekleşir.</p>

<h4>7. Hangi marka ve modelleri bulabilirim?</h4>
<p>Geniş bir marka ve model yelpazesine sahibiz. Sitemizde mevcut araçları inceleyerek istediğiniz marka ve modeli bulabilirsiniz. Ancak stoklarımız sürekli değiştiği için belirli bir modelin her zaman bulunabileceğini garanti edemeyiz.</p>

<h4>8. Garanti veya servis hizmetleri sunuyor musunuz?</h4>
<p>Bazı araçlarımız belirli bir garanti süresi altında olabilir. Servis hizmetleri için de anlaşmalı olduğumuz tamirhanelerimiz bulunmaktadır. Bu konudaki detayları talep oluşturduğunuzda size iletebiliriz.</p>

<h4>9. Fiyatlarınızda pazarlık yapılabilir mi?</h4>
<p>Fiyatlarımız genellikle sabittir ancak bazı durumlarda müşteri memnuniyetini sağlamak adına ufak pazarlıklar yapılabilir. Bu konuda iletişime geçerek detaylı bilgi alabilirsiniz.</p>

<h4>10. Hangi ödeme yöntemlerini kabul ediyorsunuz?</h4>
<p>Nakit ödeme, banka havalesi veya kredi kartı gibi çeşitli ödeme yöntemlerini kabul ediyoruz. Ödeme seçenekleri hakkında daha fazla bilgi almak için bizimle iletişime geçebilirsiniz.</p>
<h4>11. Araçlarınızın kilometreleri ne kadar?</h4>
<p>Genellikle her aracın kilometre bilgisi ilanlarımızda belirtilir. Ancak detaylı bilgi almak için bize ulaşabilirsiniz.</p>

<h4>12. Araçları nasıl satın alabilirim?</h4>
<p>İlgilendiğiniz bir aracı satın almak için bize talep oluşturabilir veya iletişim bilgilerimizden bize ulaşabilirsiniz. Satın alma süreci hakkında size rehberlik edeceğiz.</p>

<h4>13. Kullanılmış araçlarınız için garanti süresi nedir?</h4>
<p>Garanti süreleri araçların durumuna ve türüne göre değişebilir. Belirli araçlar için garanti sunduğumuzdan emin olmak için bize başvurabilirsiniz.</p>

<h4>14. Araçların teknik özellikleri nelerdir?</h4>
<p>Teknik özellikler, ilanlarımızda veya sizinle paylaşacağımız belgelerde detaylı olarak yer alır. Motor gücü, yakıt tüketimi, vites tipi gibi bilgilere kolayca erişebilirsiniz.</p>

<h4>15. Araç alımı için hangi belgeler gerekir?</h4>
<p>Araç alımı için genellikle kimlik belgesi ve sigorta gibi belgeler gerekir. Detaylı bilgi için bize başvurabilirsiniz.</p>

<h4>16. Araçların geçmişindeki kazalar hakkında bilgi alabilir miyim?</h4>
<p>Mümkün olduğunca araçların geçmişini araştırır ve kazalar hakkında bilgi sağlarız. Ancak tam detaylar için bize başvurmanız gerekmektedir.</p>

<h4>17. Araçlarınızın bakımları düzenli olarak yapılıyor mu?</h4>
<p>Evet, galerimizde bulunan araçların bakımları düzenli olarak yapılır ve gerekli kontroller gerçekleştirilir.</p>

<h4>18. Araçları nasıl rezerve edebilirim?</h4>
<p>İlgilendiğiniz bir aracı rezerve etmek için bize ulaşabilir ve rezervasyon süreci hakkında bilgi alabilirsiniz.</p>

<h4>19. Araçları nereden temin ediyorsunuz?</h4>
<p>Araçlarımızı çeşitli kaynaklardan temin ediyoruz, bunlar özel satıcılar, müzayede evleri veya takas araçları olabilir.</p>

<h4>20. Satın aldığım aracı teslim almak için ne yapmalıyım?</h4>
<p>Satın aldığınız aracı teslim almak için randevu almanız ve gerekli belgeleri hazırlamanız yeterlidir. Teslimat süreci hakkında daha fazla bilgi için bize ulaşabilirsiniz.</p>
